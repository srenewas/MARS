package com.seenu.naya;

public class CharacterAddition {
    public static void main(String[] args) {
        System.out.println('j' + 'a' + 'v' + 'a'); //418 (106+97+118+97)
        System.out.println("j" + 'a' + 'v' + 'a'); // java
        System.out.println('j' + 1 + "v" + 'a'); // 107va (106+1=107va)
        System.out.println('B' + "a" + 'v' + 'a'); // Bava
    }
}
